def emotionKrtoEng(emotion):
    if emotion == "기쁨":
        return 'pleasure'
    elif emotion == "기대":
        return 'hope'
    elif emotion == "신뢰":
        return 'trust'
    elif emotion == "공포":
        return 'fear'
    elif emotion == "놀라움":
        return 'surprise'
    elif emotion == "슬픔":
        return 'sadness'
    elif emotion == "혐오":
        return 'aversion'
    elif emotion == "분노":
        return 'anger'
    else:
        return emotion



